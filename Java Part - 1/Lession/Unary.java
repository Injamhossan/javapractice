public class Unary {
    public static void main(String[] args) {
        int a = 10;
        int b = -a;
        int c = b;
        System.out.println(c);

        int x = 5;
        x = x + 1;
        System.out.println(x);

        x+=1;
        x++;
        System.out.println(x);
    }
}
